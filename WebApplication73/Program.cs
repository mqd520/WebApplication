using WebApplication73.Api.WebApplication24;

namespace WebApplication73
{
    public class Program
    {
        public static void Main(string[] args)
        {
            var builder = WebApplication.CreateBuilder(args);

            // Add services to the container.
            builder.Services.AddControllersWithViews();

            //WebRequest.DefaultWebProxy = new WebProxy("http://localhost:8888");
            builder.Services.AddHttpApi<IUserApi>(options =>
            {
                options.HttpHost = new Uri(builder.Configuration["WebApplication24Address"]!);
            });

            var app = builder.Build();

            // Configure the HTTP request pipeline.
            if (!app.Environment.IsDevelopment())
            {
                app.UseExceptionHandler("/Home/Error");
            }
            app.UseStaticFiles();

            app.UseRouting();

            app.UseAuthorization();

            app.MapControllerRoute(
                name: "default",
                pattern: "{controller=Home}/{action=Index}/{id?}");

            app.Run();
        }
    }
}
